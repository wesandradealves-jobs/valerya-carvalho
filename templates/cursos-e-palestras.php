		<h2>Facilitação Learning 3.0</h2>
		<p>
			Criar conhecimento através do aprendizado emergente: é assim que se faz em sistemas complexos.
			O Learning 3.0 dá significado e importância para as experiências e ideias dos profissionais do sec. XXI, que veem na colaboração a grande saída para a resolução de problemas e inovação.
			Valérya Carvalho, facilitadora licenciada do Learning 3.0, organiza e facilita Learning Shots sobre qualquer tema que precisa de solução na sua organização.
			<br/><br/>
			<b>Learning Shot</b>  são eventos de aprendizagem emergente. Nesses eventos, os participantes se misturam em volta de problemas reais e constroem novos conhecimentos, de forma facilitada, através do confronto entre experiências e ideias com o 'mundo real'.
			São apresentadas opiniões de convidados, materiais curados sobre o tema e ferramentas de facilitação para suportar o aprendizado emergente. As interações entre os participantes e convidados serão facilitadas durante todo o evento. 
			Learning Shots podem ser públicos ou organizados sob medida para sua organização.
			<br/><br/>
			<b>Temas sugeridos:</b>
		</p>
		<ul>
			<li><p>· Como promover a auto-organização de equipes?</p></li>
			<li><p>· Gestão sem gerentes: Como as decisões são tomadas em uma empresa que não possui hierarquia formal?</p></li>
			<li><p>· Como resolver o problema de comunicação nas equipes e na empresa?</p></li>
			<li><p>· Como facilitar conversas difíceis?</p></li>
			<li><p>· Ou qualquer outro tema que você queira tratar...</p></li>
		</ul>
